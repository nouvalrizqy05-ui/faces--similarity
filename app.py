    "def cosine_similarity(z1, z2):\n",
    "    \"\"\"Cosine similarity antara dua vektor PCA.\"\"\"\n",
    "    n1, n2 = np.linalg.norm(z1), np.linalg.norm(z2)\n",
    "    if n1 == 0 or n2 == 0: return 0.0\n",
    "    return float(np.dot(z1, z2) / (n1 * n2))\n",
    "\n",
