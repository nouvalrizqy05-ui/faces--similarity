    "## 7. Implementasi PCA via SVD dari Scratch\n",
    "\n",
    "**Tanpa sklearn** — mengimplementasikan langsung sesuai panduan:\n",
    "\n",
    "$$X_c = U \\Sigma V^T$$\n",
