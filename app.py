    "# Hitung eigenvalue dari singular value\n",
    "m = Xc.shape[0]\n",
    "eigenvalues = (sigma ** 2) / (m - 1)\n",
    "\n",
    "# Explained variance ratio\n",
    "ev_total = np.sum(eigenvalues)\n",
    "evr      = eigenvalues / ev_total  # per komponen\n",
    "cumevr   = np.cumsum(evr)          # kumulatif\n",
    "\n",
    "# Komponen yang dipilih\n",
    "k = N_COMPONENTS\n",
    "V_k = VT[:k]          # (k, n) — eigenvectors (eigenfaces)\n",
    "\n",
    "print(f'=== Eigenvalue Report ===')\n",
    "print(f'k = {k} komponen dipilih')\n",
    "print(f'Explained variance dengan k={k}: {cumevr[k-1]*100:.2f}%')\n",
    "print(f'\\nKomponen minimum untuk:')\n",
    "for thresh in [0.80, 0.90, 0.95, 0.99]:\n",
