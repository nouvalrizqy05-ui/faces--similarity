    "def reorganize_fgnet(zip_path: str, output_dir: str = 'dataset/') -> dict:\n",
    "    \"\"\"\n",
    "    Ekstrak dan reorganisasi dataset FG-NET dari flat ZIP\n",
    "    ke struktur per-orang.\n",
    "    \"\"\"\n",
    "    raw_dir = 'images_raw/'\n",
    "    os.makedirs(raw_dir, exist_ok=True)\n",
    "    os.makedirs(output_dir, exist_ok=True)\n",
    "\n",
    "    print(f'Mengekstrak {zip_path}...')\n",
    "    with zipfile.ZipFile(zip_path, 'r') as z:\n",
    "        z.extractall(raw_dir)\n",
    "\n",
    "    # Cari folder images/\n",
    "    src_folder = None\n",
    "    for root, dirs, files in os.walk(raw_dir):\n",
    "        if any(f.lower().endswith('.jpg') for f in files):\n",
    "            src_folder = root\n",
    "            break\n",
    "\n",
    "    if src_folder is None:\n",
    "        raise FileNotFoundError('Tidak ada file JPG ditemukan dalam ZIP')\n",
    "\n",
    "    pattern = re.compile(r'^(\\d{3})A\\d+[a-z]?\\.JPG$', re.IGNORECASE)\n",
    "    moved, skipped, persons = 0, 0, set()\n",
    "\n",
    "    for fname in sorted(os.listdir(src_folder)):\n",
    "        m = pattern.match(fname)\n",
    "        if not m:\n",
    "            skipped += 1\n",
    "            continue\n",
    "        person_id  = m.group(1)\n",
    "        person_dir = os.path.join(output_dir, person_id)\n",
    "        os.makedirs(person_dir, exist_ok=True)\n",
    "        shutil.copy2(\n",
    "            os.path.join(src_folder, fname),\n",
    "            os.path.join(person_dir, fname)\n",
    "        )\n",
    "        persons.add(person_id)\n",
    "        moved += 1\n",
    "\n",
    "    print(f'Selesai! {moved} file → {len(persons)} orang')\n",
    "    return {'moved': moved, 'skipped': skipped, 'persons': sorted(persons)}\n",
    "\n",
    "\n",
    "# Jalankan reorganisasi\n",
    "if not os.path.isdir(DATASET_PATH) or len(os.listdir(DATASET_PATH)) == 0:\n",
