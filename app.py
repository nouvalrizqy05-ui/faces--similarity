    "import time\n",
    "\n",
    "print(f'Menjalankan SVD pada Xc ({Xc.shape})...')\n",
    "print('(Ini bisa memakan waktu 10–30 detik)')\n",
    "\n",
    "t0 = time.time()\n",
    "# SVD — inti dari implementasi PCA kita\n",
    "# full_matrices=False → Reduced SVD (lebih efisien memori)\n",
    "U, sigma, VT = np.linalg.svd(Xc, full_matrices=False)\n",
    "t_elapsed = time.time() - t0\n",
    "\n",
    "print(f'SVD selesai dalam {t_elapsed:.1f} detik')\n",
