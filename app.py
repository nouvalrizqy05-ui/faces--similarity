    "def load_face_image(path: str, img_size=IMG_SIZE) -> np.ndarray:\n",
    "    \"\"\"Baca gambar → vektor float64 [0,1] shape (n,)\"\"\"\n",
    "    img  = cv2.imread(path)\n",
    "    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)\n",
    "    resized = cv2.resize(gray, img_size)\n",
    "    return (resized.astype(np.float64) / 255.0).flatten()\n",
