    "\n",
    "def load_dataset(dataset_path: str, img_size=IMG_SIZE):\n",
    "    \"\"\"Load semua gambar dari dataset/XXX/ → X, labels, paths\"\"\"\n",
    "    X, labels, paths = [], [], []\n",
    "    for pid in sorted(os.listdir(dataset_path)):\n",
    "        pdir = os.path.join(dataset_path, pid)\n",
    "        if not os.path.isdir(pdir): continue\n",
    "        for fname in sorted(os.listdir(pdir)):\n",
    "            if fname.lower().endswith(('.jpg','.jpeg','.png')):\n",
    "                fpath = os.path.join(pdir, fname)\n",
    "                try:\n",
    "                    X.append(load_face_image(fpath, img_size))\n",
    "                    labels.append(pid)\n",
    "                    paths.append(fpath)\n",
    "                except Exception: pass\n",
    "    return np.array(X), np.array(labels), paths\n",
